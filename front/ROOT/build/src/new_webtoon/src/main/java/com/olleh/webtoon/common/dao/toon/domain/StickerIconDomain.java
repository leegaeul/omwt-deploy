package com.olleh.webtoon.common.dao.toon.domain;

public class StickerIconDomain {
	
	private int iconseq;        //아이콘순번 (PK, 자동증가)
	private int pageSize;   	//페이지 사이즈
	private int startRowNo;	    //시작 번호
	private String defaultyn;   //기본적으로 제공되는 아이콘인지 여부 (Y/N)
	private String oniconurl;   //웹 루트에 있는 On 스티커 아이콘의 웹 URL   (기본이 아니면 NAS 경로)
	private String officonurl;  //웹 루트에 있는 Off 스티커 아이콘의 웹 URL  (기본이 아니면 NAS 경로)
	private String listiconurl; //웹 루트에 있는 목록용 스티커 아이콘의 웹 URL (기본이 아니면 NAS 경로임)
	
	
	public int getIconseq() {
		return iconseq;
	}
	public void setIconseq(int iconseq) {
		this.iconseq = iconseq;
	}
	public String getDefaultyn() {
		return defaultyn;
	}
	public void setDefaultyn(String defaultyn) {
		this.defaultyn = defaultyn;
	}
	public String getOniconurl() {
		return oniconurl;
	}
	public void setOniconurl(String oniconurl) {
		this.oniconurl = oniconurl;
	}
	public String getOfficonurl() {
		return officonurl;
	}
	public void setOfficonurl(String officonurl) {
		this.officonurl = officonurl;
	}
	public String getListiconurl() {
		return listiconurl;
	}
	public void setListiconurl(String listiconurl) {
		this.listiconurl = listiconurl;
	}
	public int getPageSize() {
		return pageSize;
	}
	public void setPageSize(int pageSize) {
		this.pageSize = pageSize;
	}
	public int getStartRowNo() {
		return startRowNo;
	}
	public void setStartRowNo(int startRowNo) {
		this.startRowNo = startRowNo;
	}
}