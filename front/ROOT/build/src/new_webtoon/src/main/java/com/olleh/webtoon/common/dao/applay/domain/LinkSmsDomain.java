package com.olleh.webtoon.common.dao.applay.domain;

public class LinkSmsDomain {
	
	private int smsseq;				// SMS순번 (PK, 자동증가)
	private int appseq;				// 추천앱순번
	private String idfg;			// ID구분 (open:오픈ID, olleh:올레ID)
	private String regid;			// 등록자ID
	private String regdt;			// 등록일시 (YYYYMMDDHHMMSS)
	private String destphone;		// 받는 사람 핸드폰 번호
	private String marketyn;        // 마켓url여부
	private String msg;				// 받는 사람 핸드폰 번호
	
	public int getSmsseq() {
		return smsseq;
	}
	public void setSmsseq(int smsseq) {
		this.smsseq = smsseq;
	}
	public int getAppseq() {
		return appseq;
	}
	public void setAppseq(int appseq) {
		this.appseq = appseq;
	}
	public String getIdfg() {
		return idfg;
	}
	public void setIdfg(String idfg) {
		this.idfg = idfg;
	}
	public String getRegid() {
		return regid;
	}
	public void setRegid(String regid) {
		this.regid = regid;
	}
	public String getRegdt() {
		return regdt;
	}
	public void setRegdt(String regdt) {
		this.regdt = regdt;
	}
	public String getDestphone() {
		return destphone;
	}
	public void setDestphone(String destphone) {
		this.destphone = destphone;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public String getMarketyn() {
		return marketyn;
	}
	public void setMarketyn(String marketyn) {
		this.marketyn = marketyn;
	}
	
}