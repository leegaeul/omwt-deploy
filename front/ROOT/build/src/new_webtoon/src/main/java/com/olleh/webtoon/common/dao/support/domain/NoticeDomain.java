package com.olleh.webtoon.common.dao.support.domain;

public class NoticeDomain 
{
	private int noticeseq;  		//공지사항순번 (PK, 자동증가)
	private String title;  			//제목
	private String contents; 		//내용
	private String htmlyn; 			//내용이 HTML인지 여부 (Y/N)
	private String displayyn; 		//pc 전시여부 (Y/N)
	private String mdisplayyn; 		//mobile 전시여부 (Y/N)
	private String directdisplayyn; //즉시전시여부 (Y/N)
	private String maindisplayyn; 	//메인화면에 전시할지 여부 (Y/N)
	private String startdt; 		//전시시작일 (YYYYMMDDHHMMSS)
	private String topyn; 			//pc 상단고정여부 (Y/N)
	private String mtopyn;   		//mobile 상단고정여부 (Y/N)
	private long readcnt; 			//조회수
	private String regdate; 		//등록일시 (YYYYMMDDHHMMSS)
	private String keyword; 		//검색 키워드
	private int startRowNo; 		//Page Block 시작번호
	private int pageSize; 			//Page 게시글 갯수
	private String mobileyn = "N"; 	//mobile 여부
	private String newyn;           //new여부(등록 후 일주일간 new상태)
	
	
	public int getNoticeseq() {
		return noticeseq;
	}
	public void setNoticeseq(int noticeseq) {
		this.noticeseq = noticeseq;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContents() {
		return contents;
	}
	public void setContents(String contents) {
		this.contents = contents;
	}
	public String getHtmlyn() {
		return htmlyn;
	}
	public void setHtmlyn(String htmlyn) {
		this.htmlyn = htmlyn;
	}
	public String getDisplayyn() {
		return displayyn;
	}
	public void setDisplayyn(String displayyn) {
		this.displayyn = displayyn;
	}
	public String getMdisplayyn() {
		return mdisplayyn;
	}
	public void setMdisplayyn(String mdisplayyn) {
		this.mdisplayyn = mdisplayyn;
	}
	public String getDirectdisplayyn() {
		return directdisplayyn;
	}
	public void setDirectdisplayyn(String directdisplayyn) {
		this.directdisplayyn = directdisplayyn;
	}
	public String getMaindisplayyn() {
		return maindisplayyn;
	}
	public void setMaindisplayyn(String maindisplayyn) {
		this.maindisplayyn = maindisplayyn;
	}
	public String getStartdt() {
		return startdt;
	}
	public void setStartdt(String startdt) {
		this.startdt = startdt;
	}
	public String getTopyn() {
		return topyn;
	}
	public void setTopyn(String topyn) {
		this.topyn = topyn;
	}
	public String getMtopyn() {
		return mtopyn;
	}
	public void setMtopyn(String mtopyn) {
		this.mtopyn = mtopyn;
	}
	public long getReadcnt() {
		return readcnt;
	}
	public void setReadcnt(long readcnt) {
		this.readcnt = readcnt;
	}
	public String getRegdate() {
		return regdate;
	}
	public void setRegdate(String regdate) {
		this.regdate = regdate;
	}
	public String getKeyword() {
		return keyword;
	}
	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}
	public int getStartRowNo() {
		return startRowNo;
	}
	public void setStartRowNo(int startRowNo) {
		this.startRowNo = startRowNo;
	}
	public int getPageSize() {
		return pageSize;
	}
	public void setPageSize(int pageSize) {
		this.pageSize = pageSize;
	}
	public String getMobileyn() {
		return mobileyn;
	}
	public void setMobileyn(String mobileyn) {
		this.mobileyn = mobileyn;
	}
	public String getNewyn() {
		return newyn;
	}
	public void setNewyn(String newyn) {
		this.newyn = newyn;
	}
}