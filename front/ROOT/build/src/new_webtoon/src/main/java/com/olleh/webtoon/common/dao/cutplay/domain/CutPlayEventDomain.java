package com.olleh.webtoon.common.dao.cutplay.domain;

public class CutPlayEventDomain
{
	private int eventtimes;			//이벤트차수
	private String startdt;			//시작일시 (YYYYMMDDHHMMSS)
	private String enddt;			//종료일시 (YYYYMMDDHHMMSS)
	
	public int getEventtimes() {
		return eventtimes;
	}
	public void setEventtimes(int eventtimes) {
		this.eventtimes = eventtimes;
	}
	public String getStartdt() {
		return startdt;
	}
	public void setStartdt(String startdt) {
		this.startdt = startdt;
	}
	public String getEnddt() {
		return enddt;
	}
	public void setEnddt(String enddt) {
		this.enddt = enddt;
	}
}