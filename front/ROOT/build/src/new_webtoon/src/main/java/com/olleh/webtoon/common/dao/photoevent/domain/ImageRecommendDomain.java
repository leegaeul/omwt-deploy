package com.olleh.webtoon.common.dao.photoevent.domain;

public class ImageRecommendDomain {

	private int imageseq; 		//이미지 순번 (FK, 복합PK)
	private int recommendno; 	//이미지번호별 순번 (복합PK, 이미지순번별로 1부터 max+1임)
	private String recommendfg; //추천구분 (good:좋아요, bad:싫어요)
	private String idfg; 		//ID구분 (open:오픈ID, olleh:올레ID)
	private String regid; 		//등록한 사용자ID
	private String regdt; 		//등록일시 (YYYYMMDDHHMMSS)
	
	public int getImageseq() {
		return imageseq;
	}
	public void setImageseq(int imageseq) {
		this.imageseq = imageseq;
	}
	public int getRecommendno() {
		return recommendno;
	}
	public void setRecommendno(int recommendno) {
		this.recommendno = recommendno;
	}
	public String getRecommendfg() {
		return recommendfg;
	}
	public void setRecommendfg(String recommendfg) {
		this.recommendfg = recommendfg;
	}
	public String getIdfg() {
		return idfg;
	}
	public void setIdfg(String idfg) {
		this.idfg = idfg;
	}
	public String getRegid() {
		return regid;
	}
	public void setRegid(String regid) {
		this.regid = regid;
	}
	public String getRegdt() {
		return regdt;
	}
	public void setRegdt(String regdt) {
		this.regdt = regdt;
	}
}
